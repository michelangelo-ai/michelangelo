#!/bin/bash

set -e

if [ "$#" -ne 1 ]; then
    echo "Generate gRPC service definition for a CRD type."
    echo "$0 <Crd name>"
    echo "Example: $0 Model"
    exit
fi

CRD=$1

SCRIPT_DIR="$(cd "$(dirname "$(readlink -f "${BASH_SOURCE[0]}")")" && pwd)"

if [[ ! "${CRD}" =~ ^[A-Z].*$ ]]; then
  echo "CRD name <${CRD}> does not start with a capital letter"
fi

echo "CRD name: ${CRD}"
SNAKE_CRD="$(echo "${CRD}" | sed -r 's/([A-Z])/_\1/g' | sed 's/^_//' | tr '[:upper:]' '[:lower:]')"
echo "CRD name snake case: ${SNAKE_CRD}"

PROTO_PATH="${WORKSPACE_ROOT}/proto/api/v2"
OUTPUT="${PROTO_PATH}/${SNAKE_CRD}_svc.proto"

cat > "${OUTPUT}"  << EOF
// This file is automatically generated by the following command:
//
// Important:
// - This file is part of the automated build process and should not be manually edited.
// - Any manual changes will be overwritten the next time the generation command is run.
//
// If modifications are needed, update the source proto files and run scripts used in the generation process.
// For more information, refer to the documentation or contact the maintainers.
//
EOF

sed "s/CRDCamelName/${CRD}/g" "${SCRIPT_DIR}/grpc-svc-gen.tmpl" \
    | sed "s/crd_snake_name/${SNAKE_CRD}/g" \
    >> "${OUTPUT}"

echo "gRPC service definition written to: ${OUTPUT}"
