// -----------------------------------------------------------------------------
// MOCK: Simulates proto/api/v2/model.proto generated types
// -----------------------------------------------------------------------------

// MockV2Model simulates v2.Model (the base CRD type)
type MockV2Model struct {
	Name string
	Spec *MockV2ModelSpec
}

// MockV2ModelSpec simulates v2.ModelSpec
type MockV2ModelSpec struct {
	Owner       string
	Description string
	Vendor      string
	Kind        int32
}

// Validate simulates the base validation (minimal rules)
// Generated by protoc-gen-validation on v2/model.proto
func (m *MockV2Model) Validate(prefix string) error {
	// Base proto might have minimal or no validation
	// This is intentional - OSS version is permissive
	return nil
}

// -----------------------------------------------------------------------------
// MOCK: Simulates proto/api/v2_ext/model_ext.proto generated types
// -----------------------------------------------------------------------------

// MockV2ExtModelSpec simulates v2_ext.ModelSpec (stricter validation)
type MockV2ExtModelSpec struct {
	Owner       string
	Description string
	Vendor      string
	Kind        int32
}

// Validate simulates the ext validation (stricter rules)
// Generated by protoc-gen-validation on v2_ext/model_ext.proto
//
// The ext proto would have annotations like:
//
//	message ModelSpec {
//	  string owner = 1 [(michelangelo.api.validation) = {required: true}];
//	  string description = 2 [(michelangelo.api.validation) = {max_length: "2000"}];
//	  string vendor = 3 [(michelangelo.api.validation) = {required: true, max_length: "100"}];
//	  int32 kind = 4 [(michelangelo.api.validation) = {min: "1"}];
//	}
func (m *MockV2ExtModelSpec) Validate(prefix string) error {
	// Stricter validation for internal use
	if m.Owner == "" {
		return errors.New(prefix + "owner is required")
	}
	if len(m.Description) > 2000 {
		return errors.New(prefix + "description must be at most 2000 characters")
	}
	if m.Vendor == "" {
		return errors.New(prefix + "vendor is required")
	}
	if len(m.Vendor) > 100 {
		return errors.New(prefix + "vendor must be at most 100 characters")
	}
	if m.Kind < 1 {
		return errors.New(prefix + "kind must be at least 1")
	}
	return nil
}

// -----------------------------------------------------------------------------
// MOCK: Simulates proto/api/v2_ext/register.go
// This is what would be in the v2_ext package to register validators
// -----------------------------------------------------------------------------

// registerMockV2ExtValidators simulates what init() in v2_ext/register.go does
func registerMockV2ExtValidators() {
	// Register validator for v2.Model
	RegisterExtValidator("*api.MockV2Model", func(obj interface{}) error {
		model := obj.(*MockV2Model)
		if model.Spec == nil {
			return nil // No spec to validate
		}

		// Copy fields from v2.ModelSpec to v2_ext.ModelSpec
		ext := &MockV2ExtModelSpec{
			Owner:       model.Spec.Owner,
			Description: model.Spec.Description,
			Vendor:      model.Spec.Vendor,
			Kind:        model.Spec.Kind,
		}

		// Use the GENERATED Validate() method on ext type
		return ext.Validate("spec.")
	})
}

// -----------------------------------------------------------------------------
// TESTS: Demonstrate the complete flow
// -----------------------------------------------------------------------------

// TestCompleteExtValidationFlow shows the entire system working together
func TestCompleteExtValidationFlow(t *testing.T) {
	defer ClearExtValidators()

	// Step 1: Simulate importing v2_ext package (triggers init())
	registerMockV2ExtValidators()

	// Verify registration happened
	assert.True(t, HasExtValidator("*api.MockV2Model"))

	// Step 2: Create test objects
	tests := []struct {
		name        string
		model       *MockV2Model
		wantErr     bool
		errContains string
	}{
		{
			name: "valid model passes all validation",
			model: &MockV2Model{
				Name: "my-model",
				Spec: &MockV2ModelSpec{
					Owner:       "user@example.com",
					Description: "A valid model",
					Vendor:      "openai",
					Kind:        1,
				},
			},
			wantErr: false,
		},
		{
			name: "missing owner fails ext validation",
			model: &MockV2Model{
				Name: "my-model",
				Spec: &MockV2ModelSpec{
					Owner:       "", // Empty - fails ext validation!
					Description: "A model without owner",
					Vendor:      "openai",
					Kind:        1,
				},
			},
			wantErr:     true,
			errContains: "owner is required",
		},
		{
			name: "missing vendor fails ext validation",
			model: &MockV2Model{
				Name: "my-model",
				Spec: &MockV2ModelSpec{
					Owner:       "user@example.com",
					Description: "A model without vendor",
					Vendor:      "", // Empty - fails ext validation!
					Kind:        1,
				},
			},
			wantErr:     true,
			errContains: "vendor is required",
		},
		{
			name: "invalid kind fails ext validation",
			model: &MockV2Model{
				Name: "my-model",
				Spec: &MockV2ModelSpec{
					Owner:       "user@example.com",
					Description: "A model with invalid kind",
					Vendor:      "openai",
					Kind:        0, // Zero - fails ext validation (must be >= 1)
				},
			},
			wantErr:     true,
			errContains: "kind must be at least 1",
		},
		{
			name: "description too long fails ext validation",
			model: &MockV2Model{
				Name: "my-model",
				Spec: &MockV2ModelSpec{
					Owner:       "user@example.com",
					Description: string(make([]byte, 2001)), // Too long!
					Vendor:      "openai",
					Kind:        1,
				},
			},
			wantErr:     true,
			errContains: "description must be at most 2000 characters",
		},
		{
			name:    "nil spec passes validation",
			model:   &MockV2Model{Name: "my-model", Spec: nil},
			wantErr: false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			// Step 3: Run base validation (would be called by handler)
			err := tt.model.Validate("")
			require.NoError(t, err, "base validation should always pass (permissive)")

			// Step 4: Run ext validation (automatically called by handler)
			err = ValidateExt(tt.model)

			if tt.wantErr {
				require.Error(t, err)
				assert.Contains(t, err.Error(), tt.errContains)
			} else {
				assert.NoError(t, err)
			}
		})
	}
}

// TestExtValidationDisabledWithoutImport shows that ext validation is a no-op
// when the ext package is not imported
func TestExtValidationDisabledWithoutImport(t *testing.T) {
	defer ClearExtValidators()

	// Don't register any validators (simulates not importing v2_ext)

	// Even invalid data passes because no ext validator is registered
	model := &MockV2Model{
		Name: "my-model",
		Spec: &MockV2ModelSpec{
			Owner:  "", // Would fail if ext was imported
			Vendor: "", // Would fail if ext was imported
			Kind:   0,  // Would fail if ext was imported
		},
	}

	err := ValidateExt(model)
	assert.NoError(t, err, "without ext import, validation should pass")
}

// TestHandlerValidationFlow simulates how the validation handler uses this
func TestHandlerValidationFlow(t *testing.T) {
	defer ClearExtValidators()

	// Register ext validators (simulates importing v2_ext)
	registerMockV2ExtValidators()

	// Simulate ValidationHandler.ValidateCreate
	validateCreate := func(obj interface{}) error {
		// Step 1: Base validation
		if v, ok := obj.(interface{ Validate(string) error }); ok {
			if err := v.Validate(""); err != nil {
				return err
			}
		}

		// Step 2: Extension validation
		return ValidateExt(obj)
	}

	// Test with valid object
	validModel := &MockV2Model{
		Name: "valid-model",
		Spec: &MockV2ModelSpec{
			Owner:  "user@example.com",
			Vendor: "openai",
			Kind:   1,
		},
	}
	err := validateCreate(validModel)
	assert.NoError(t, err)

	// Test with invalid object
	invalidModel := &MockV2Model{
		Name: "invalid-model",
		Spec: &MockV2ModelSpec{
			Owner:  "", // Invalid!
			Vendor: "openai",
			Kind:   1,
		},
	}
	err = validateCreate(invalidModel)
	require.Error(t, err)
	assert.Contains(t, err.Error(), "owner is required")
}

