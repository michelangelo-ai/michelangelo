// Manager interface defines functions to work with runtime.Object such as serialize/deserialize,
// and converting between MySQLObject.
type Manager interface {
	// Scheme returns the registered scheme
	Scheme() *runtime.Scheme
	// Encode writes an object to a stream
	Encode(runtime.Object, io.Writer) error
	// Decode attempts to deserialize the provided data into the object
	Decode([]byte, *schema.GroupVersionKind, runtime.Object) (runtime.Object, *schema.GroupVersionKind, error)
	// GetIndexPathToKeyMap gets the map of index path to key for a given gvk
	GetIndexPathToKeyMap(schema.GroupVersionKind) (map[string]string, error)
	// FromRuntimeObject converts runtime.Object into MySQLObject
	FromRuntimeObject(runtime.Object, *MySQLObject) error
	// ToRuntimeObject converts MySQLObject into runtime.Object
	ToRuntimeObject(MySQLObject, runtime.Object) error
}

type managerImpl struct {
	scheme            *runtime.Scheme
	encoder           runtime.Encoder
	decoder           runtime.Decoder
	indexPathToKeyMap map[schema.GroupVersionKind]map[string]string
}

// Params are the dependencies to build objManger.
type Params struct {
	fx.In

	Scheme       *runtime.Scheme
	GroupVersion runtime.GroupVersioner
	IndexMaps    []map[schema.GroupVersionKind]map[string]string `group:"indexMaps"`
}

// NewtManager registers runtime.Scheme and a list of per GVK index path to index key maps to objManager.
// 1) For the registered runtime.Scheme, the MySQLObject manager creates Encoder and Decoder
// for serialize and deserialize API MySQLObject to and from JSON.
// The Encoder will encode the objects into the provided group version.
// The Decoder is a universal Decoder that recognize all the available group versions.
// 2) The registered per GVK maps will be used to validate the field selector.
func NewtManager(params Params) (Manager, error) {
	const defaultMediaType = "application/json"
	codecFactory := serializer.NewCodecFactory(params.Scheme)
	objManager := &managerImpl{}
	info, ok := runtime.SerializerInfoForMediaType(codecFactory.SupportedMediaTypes(), defaultMediaType)
	if ok == false {
		return nil, fmt.Errorf("failed to init serializer for default media type: %v", defaultMediaType)
	}

	objManager.scheme = params.Scheme
	objManager.encoder = codecFactory.EncoderForVersion(info.Serializer, params.GroupVersion)
	objManager.decoder = codecFactory.UniversalDecoder()
	objManager.indexPathToKeyMap = make(map[schema.GroupVersionKind]map[string]string)

	for _, indexMap := range params.IndexMaps {
		for gvk, perGvkMap := range indexMap {
			if _, found := objManager.indexPathToKeyMap[gvk]; found {
				return nil, fmt.Errorf("failed to init storage MySQLObject manager. Duplicated gvk: %v", gvk)
			}
			objManager.indexPathToKeyMap[gvk] = perGvkMap
		}
	}

	return objManager, nil
}

type protoMsg interface {
	Marshal() ([]byte, error)
	Unmarshal([]byte) error
}

// MySQLObject represents a CRD object stored in MySQL
type MySQLObject struct {
	// Kind is the CRD Kind of the MySQLObject.
	Kind string

	// GroupVer is the group/version of the CRD Kind.
	GroupVer string

	// JSON is the serialized MySQLObject in JSON format.
	JSON []byte

	// Proto is the serialized MySQLObject in ProtoBuf format.
	Proto []byte

	// Namespace defines the space within which each name must be unique.
	Namespace string

	// Name must be unique within a namespace.
	Name string

	// UID is the unique in time and space value for this MySQLObject. It is typically generated by
	// the k8s server on successful creation of a resource and is not allowed to change on PUT
	// operations.
	UID string

	// ResourceVersion in uint64 type.
	// For Kubernetes users ResourceVersion is an opaque string. Internally, ResourceVersion is the uint64 version
	// number from ETCD. Therefore, in the storage layer, ResourceVersion is an uint64 number.
	ResourceVersion uint64

	// CreationTimestamp is a timestamp representing the server time when this MySQLObject was created.
	CreationTimestamp time.Time

	// UpdateTimestamp is a timestamp representing the server time when this MySQLObject was updated.
	// Since Kubernetes does not provide this info, this field is populated when updating to MySQL.
	UpdateTimestamp time.Time

	// DeletionTimestamp is date and time at which this resource is deleted.
	DeletionTimestamp *time.Time

	// Labels are stored separately from the serialized MySQLObject.
	// All labels are indexed in the metadataStorageImpl.
	Labels map[string]string

	// Annotations are stored separately from the serialized MySQLObject.
	// Annotations are not indexed in the metadataStorageImpl, but maybe indexed in SIA.
	Annotations map[string]string
}

// Scheme ...
func (m *managerImpl) Scheme() *runtime.Scheme {
	return m.scheme
}

// Encode ...
func (m *managerImpl) Encode(obj runtime.Object, w io.Writer) error {
	return m.encoder.Encode(obj, w)
}

// Decode ...
func (m *managerImpl) Decode(data []byte, gvk *schema.GroupVersionKind, obj runtime.Object) (runtime.Object,
	*schema.GroupVersionKind, error) {
	return m.decoder.Decode(data, gvk, obj)
}

// GetIndexPathToKeyMap ...
func (m *managerImpl) GetIndexPathToKeyMap(gvk schema.GroupVersionKind) (map[string]string, error) {
	indexPathToKeyMap, ok := m.indexPathToKeyMap[gvk]
	if !ok {
		return nil, status.Errorf(codes.Unimplemented,
			"unsupported object for field indexing.  Check if the gvk is registered. gvk: %v", gvk)
	}
	return indexPathToKeyMap, nil
}

// ToRuntimeObject ...
func (m *managerImpl) ToRuntimeObject(from MySQLObject, to runtime.Object) error {
	objProto, _ := interface{}(to).(protoMsg)
	err := objProto.Unmarshal(from.Proto)
	if err != nil {
		return fmt.Errorf("Proto Unmarshal failed for kind: [%s], namespace: [%s], UID: [%s]: %v",
			from.Kind, from.Namespace, from.UID, err)
	}

	accessor, err := meta.Accessor(to)
	if err != nil {
		return err
	}
	// set managed fields to nil since it's huge but not needed
	accessor.SetManagedFields(nil)
	accessor.SetResourceVersion(strconv.FormatUint(from.ResourceVersion, 10))

	// proto marshal & unmarshal will not preserve the APIVersion and Kind, so we need to set it explicitly
	gvk, err := apiutil.GVKForObject(to, m.scheme)
	if err != nil {
		return err
	}
	to.GetObjectKind().SetGroupVersionKind(gvk)

	return nil
}

// FromRuntimeObject ...
func (m *managerImpl) FromRuntimeObject(from runtime.Object, to *MySQLObject) error {
	gvk, err := apiutil.GVKForObject(from, m.scheme)
	if err != nil {
		return err
	}
	to.Kind = gvk.Kind
	to.GroupVer = gvk.GroupVersion().String()
	accessor, err := meta.Accessor(from)
	if err != nil {
		return err
	}
	// set managed fields to nil since it's huge but not needed
	accessor.SetManagedFields(nil)
	to.Namespace = accessor.GetNamespace()
	to.Name = accessor.GetName()
	to.UID = string(accessor.GetUID())

	// strip resource version from obj.Bytes
	resVer := accessor.GetResourceVersion()
	if resVer != "" {
		to.ResourceVersion, err = strconv.ParseUint(resVer, 10, 64)
		if err != nil {
			return err
		}
		accessor.SetResourceVersion("")
		defer accessor.SetResourceVersion(resVer)
	} else {
		to.ResourceVersion = 0
	}
	to.CreationTimestamp = accessor.GetCreationTimestamp().Time
	if accessor.GetDeletionTimestamp() != nil {
		to.DeletionTimestamp = &accessor.GetDeletionTimestamp().Time
	}
	to.Labels = accessor.GetLabels()
	to.Annotations = accessor.GetAnnotations()

	buf := &bytes.Buffer{}
	err = m.encoder.Encode(from, buf)
	if err != nil {
		return err
	}
	to.JSON = buf.Bytes()

	objProto, _ := interface{}(from).(protoMsg)
	bytesProto, _ := objProto.Marshal()
	to.Proto = bytesProto

	// add resource version back to runtimeObject
	if resVer != "" {
		accessor.SetResourceVersion(resVer)
	}

	return nil
}
