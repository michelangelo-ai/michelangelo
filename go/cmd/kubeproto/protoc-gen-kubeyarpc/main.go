package main

import (
	"bytes"
	"log"
	"os"
	"strings"

	"github.com/michelangelo-ai/michelangelo/go/kubeproto/pboptions"
	"github.com/michelangelo-ai/michelangelo/go/kubeproto/templates"
	"github.com/michelangelo-ai/michelangelo/go/kubeproto/util"
	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/reflect/protoregistry"
	"google.golang.org/protobuf/types/descriptorpb"
	"google.golang.org/protobuf/types/pluginpb"
)

var logger = log.New(os.Stderr, "", 0)

func generateFile(gen *protogen.Plugin, file *protogen.File, extTypes *protoregistry.Types) *protogen.GeneratedFile {
	options, err := pboptions.ReadOptions(extTypes, file.Proto.Options)

	if err != nil {
		logger.Panicf("Failed to read protobuf options: %v", err)
	}

	filename := file.GeneratedFilenamePrefix + ".pb.kubeyarpc.go"
	g := gen.NewGeneratedFile(filename, file.GoImportPath)
	g.P("// Code generated by protoc-gen-kubeyarpc. DO NOT EDIT.")
	g.P()
	g.P("package ", file.GoPackageName)
	g.P()

	if options == nil || !options.Bool("has_group_info") {
		hasCrdImport := false
		for _, msg := range file.Messages {
			pbOptions := msg.Desc.Options().(*descriptorpb.MessageOptions)
			options, err := pboptions.ReadOptions(extTypes, pbOptions)
			if err != nil {
				logger.Panicf("Failed to parse the options of message %v: %v", msg.GoIdent.GoName, err)
			}
			if options.Bool("has_resource") {
				if !hasCrdImport {
					g.P(templates.CrdSvcHandlerImports)
					hasCrdImport = true
				}
				kindName := msg.GoIdent.GoName
				lowerKindName := strings.ToLower(kindName[0:1]) + kindName[1:]

				lowerPluralName := options.String("resource.name")
				if lowerPluralName == "" {
					lowerPluralName = lowerKindName + "s"
				}

				var buf bytes.Buffer
				crdInfo := struct {
					KindName        string
					LowerKindName   string
					LowerPluralName string
				}{
					KindName:        kindName,
					LowerKindName:   lowerKindName,
					LowerPluralName: lowerPluralName,
				}
				templates.CrdSvcHandler.Execute(&buf, crdInfo)
				g.Write(buf.Bytes())
			}
		}
	}

	return g
}

func generate(reqData []byte) *pluginpb.CodeGeneratorResponse {
	gen, extTypes, err := util.GetPluginAndExtensions(reqData, false)
	if err != nil {
		logger.Panic(err)
	}

	for _, f := range gen.Files {
		// skip the proto file that don't need to generate go code,
		// such as imported proto files
		if !f.Generate {
			continue
		}
		generateFile(gen, f, extTypes)
	}

	return gen.Response()
}

func main() {
	reqData := util.ReadRequest()
	resp := generate(reqData)
	util.WriteResponse(resp)
}
