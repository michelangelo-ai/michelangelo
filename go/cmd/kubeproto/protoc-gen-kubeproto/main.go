func genImmutability(crdName string, crdBuf *bytes.Buffer, crdOptions *pboptions.Options) {
	crdIsImmutable := false
	if crdOptions.Bool("resource.immutable") {
		crdIsImmutable = true
	}
	typeInfo := struct {
		Name      string
		Immutable bool
	}{crdName, crdIsImmutable}
	templates.CRDImmutability.Execute(crdBuf, typeInfo)
	crdBuf.Write([]byte("\n"))
}

func genCRDIndexedFields(crdName string, crdRootMsg *protogen.Message, crdBuf *bytes.Buffer, crdOptions *pboptions.Options,
	groupInfo *groupinfo.GroupInfo) {
	indexedFields := util.ParseIndexedFields(crdRootMsg, crdOptions)

	typeInfo := struct {
		Name string
	}{crdName}
	templates.CRDGetIndexedFieldsHeader.Execute(crdBuf, typeInfo)

	declareVar := false
	for _, field := range indexedFields {
		// generate clause that check the message path
		checkMessagePathClause := "\tif m != nil "
		fieldGoPath := "m"
		for idx := 0; idx < len(field.GoPaths); idx++ {
			path := field.GoPaths[idx]
			if path == "Metadata" || path == "TypeMeta" {
				continue
			}
			fieldGoPath += "." + path
			if idx == len(field.GoPaths)-1 && field.HasFlag(util.IndexFlagPrimitive) {
				// Primitive fields are not generated as pointers. Thus, no need to check if it is null.
				break
			}
			if idx == 0 {
				// Spec and Status are not generated as pointers. Thus, no need to check if it is null.
				continue
			}
			checkMessagePathClause += "&& " + fieldGoPath + " != nil "
		}

		if field.HasFlag(util.IndexFlagPrimitive) {
			if !declareVar {
				crdBuf.Write([]byte("\tvar indexedField storage.IndexedField\n\n"))
				declareVar = true
			}
			statement := "\tindexedField.Key = \"" + field.Key + "\"\n"
			crdBuf.Write([]byte(statement))
			crdBuf.Write([]byte(checkMessagePathClause))
			crdBuf.Write([]byte("{\n"))
			statement = "\t\tindexedField.Value = " + fieldGoPath
			if field.HasFlag(util.IndexFlagEnum) {
				statement += ".String()"
			}
			statement += "\n"
			crdBuf.Write([]byte(statement))
			crdBuf.Write([]byte("\t} else {\n"))
			crdBuf.Write([]byte("\t\tindexedField.Value = nil\n"))
			crdBuf.Write([]byte("\t}\n"))
			crdBuf.Write([]byte("\tindexedFields = append(indexedFields, indexedField)\n\n"))
		} else {
			crdBuf.Write([]byte("\t{\n"))
			arrayDeclaration := "\t\tindexedSubFields := make([]storage.IndexedField, " + strconv.Itoa(len(field.SubFields)) + ")\n"
			crdBuf.Write([]byte(arrayDeclaration))
			for i, subField := range field.SubFields {
				statement := "\t\tindexedSubFields[" + strconv.Itoa(i) + "].Key = \"" + subField.Key + "\"\n"
				crdBuf.Write([]byte(statement))
			}
			checkMessagePathClause = "\t" + checkMessagePathClause
			crdBuf.Write([]byte(checkMessagePathClause))
			crdBuf.Write([]byte("{\n"))
			for i, subField := range field.SubFields {
				statement := "\t\t\tindexedSubFields[" + strconv.Itoa(i) + "].Value = " + fieldGoPath + "." + subField.GoPath + "\n"
				crdBuf.Write([]byte(statement))
			}
			crdBuf.Write([]byte("\t\t}\n"))
			crdBuf.Write([]byte("\t\tindexedFields = append(indexedFields, indexedSubFields...)\n"))
			crdBuf.Write([]byte("\t}\n\n"))
		}
	}
	crdBuf.Write([]byte("\treturn indexedFields\n}\n\n"))

	// Generate IndexesPathToKeyMap for this gvk
	typeInfo2 := struct {
		Group   string
		Version string
		Kind    string
	}{groupInfo.Name, groupInfo.Version, crdName}
	templates.CRDIndexesPathToKeyMapHeader.Execute(crdBuf, typeInfo2)

	for _, field := range indexedFields {
		if field.HasFlag(util.IndexFlagPrimitive) {
			crdBuf.Write([]byte("\tIndexesPathToKeyMap[gvk][\"" + field.ProtoPath + "\"] = \"" + field.Key + "\"\n"))
		} else {
			for _, subField := range field.SubFields {
				crdBuf.Write([]byte("\tIndexesPathToKeyMap[gvk][\"" + subField.ProtoPath + "\"] = \"" + subField.Key + "\"\n"))
			}
		}
	}

	crdBuf.Write([]byte("}\n\n"))
}

func findBlobFields(curMsg *protogen.Message, pathPrefix string, blobFields *[]string,
	processedMessageTypes *map[protogen.GoIdent]bool, extTypes *protoregistry.Types) {
	for _, field := range curMsg.Fields {
		options, err := util.GetFieldOptions(field, extTypes)
		if err != nil {
			logger.Panicf("Failed to parse the options for field %s: %v", field.Desc.TextName(), err)
		}

		newPrefix := strings.Trim(pathPrefix+"."+field.GoName, ".")
		if options.Bool("external_storage") {
			*blobFields = append(*blobFields, newPrefix)
		}

		// Only find blob fields in messages that are declared in the same package (e.g. michelangelo.api.v2)
		if field.Message != nil && field.Message.GoIdent.GoImportPath == curMsg.GoIdent.GoImportPath {
			// Maintain processed message types along the recursion path to avoid getting into infinite recursion
			if _, found := (*processedMessageTypes)[field.Message.GoIdent]; !found {
				(*processedMessageTypes)[field.Message.GoIdent] = true
				// When recursing into a repeated field, mark child paths with "[]" so the
				// code generator knows to emit a range loop instead of a direct assignment.
				childPrefix := newPrefix
				if field.Desc.IsList() {
					childPrefix = newPrefix + "[]"
				}
				findBlobFields(field.Message, childPrefix, blobFields, processedMessageTypes, extTypes)
				delete(*processedMessageTypes, field.Message.GoIdent)
			}
		}
	}
}

// blobFieldSegment is one dot-separated component of a blob field path.
// The repeated flag is true when the field at this position is a slice, meaning
// the code generator must emit a range loop rather than a direct field access.
type blobFieldSegment struct {
	name     string
	repeated bool
}

// parsePathSegments splits a dot-separated path (e.g. "Status.Steps[].Input") into
// segments, stripping the "[]" suffix that findBlobFields attaches to repeated fields.
func parsePathSegments(path string) []blobFieldSegment {
	parts := strings.Split(path, ".")
	segs := make([]blobFieldSegment, len(parts))
	for i, p := range parts {
		if strings.HasSuffix(p, "[]") {
			segs[i] = blobFieldSegment{name: strings.TrimSuffix(p, "[]"), repeated: true}
		} else {
			segs[i] = blobFieldSegment{name: p, repeated: false}
		}
	}
	return segs
}

// hasRepeatedSegment reports whether a blob field path contains a repeated segment marker.
func hasRepeatedSegment(path string) bool {
	return strings.Contains(path, "[]")
}

// genClearRepeatedBlobField recursively emits Go code to nil out a blob field that is
// nested inside one or more repeated (slice) fields.
//
//   - segs: remaining path segments from rootExpr to the leaf blob field.
//   - rootExpr: Go expression for the current base object ("m" at the top, a loop var inside loops).
//   - indent: current indentation string.
//   - loopIdx: counter used to generate unique loop variable names (_v0, _v1, …).
func genClearRepeatedBlobField(segs []blobFieldSegment, rootExpr, indent string, loopIdx int, crdBuf *bytes.Buffer) {
	// Find the first repeated segment in the remaining path.
	firstRepIdx := -1
	for i, s := range segs {
		if s.repeated {
			firstRepIdx = i
			break
		}
	}

	if firstRepIdx == -1 {
		// No more repeated segments — emit nil-guard + assignment for the remaining path.
		guard := ""
		cur := rootExpr
		for _, s := range segs {
			cur += "." + s.name
			if len(guard) > 0 {
				guard += " && "
			}
			guard += cur + " != nil"
		}
		leafExpr := cur
		if guard != "" {
			crdBuf.Write([]byte(fmt.Sprintf("%sif %s {\n", indent, guard)))
			crdBuf.Write([]byte(fmt.Sprintf("%s\t%s = nil\n", indent, leafExpr)))
			crdBuf.Write([]byte(fmt.Sprintf("%s}\n", indent)))
		} else {
			crdBuf.Write([]byte(fmt.Sprintf("%s%s = nil\n", indent, leafExpr)))
		}
		return
	}

	// Build the range expression: rootExpr + all segments up to and including the repeated one.
	rangeExpr := rootExpr
	for _, s := range segs[:firstRepIdx+1] {
		rangeExpr += "." + s.name
	}

	// Build a nil guard for any non-repeated pointer fields that precede the repeated segment.
	// "Spec" and "Status" are struct-embedded on CRD types and are never nil; skip them.
	prefixGuard := ""
	cur := rootExpr
	for _, s := range segs[:firstRepIdx] {
		cur += "." + s.name
		if s.name == "Spec" || s.name == "Status" {
			continue
		}
		if prefixGuard != "" {
			prefixGuard += " && "
		}
		prefixGuard += cur + " != nil"
	}

	loopVar := fmt.Sprintf("_v%d", loopIdx)

	emitLoop := func(ind string) {
		crdBuf.Write([]byte(fmt.Sprintf("%sfor _, %s := range %s {\n", ind, loopVar, rangeExpr)))
		crdBuf.Write([]byte(fmt.Sprintf("%s\tif %s != nil {\n", ind, loopVar)))
		genClearRepeatedBlobField(segs[firstRepIdx+1:], loopVar, ind+"\t\t", loopIdx+1, crdBuf)
		crdBuf.Write([]byte(fmt.Sprintf("%s\t}\n", ind)))
		crdBuf.Write([]byte(fmt.Sprintf("%s}\n", ind)))
	}

	if prefixGuard != "" {
		crdBuf.Write([]byte(fmt.Sprintf("%sif %s {\n", indent, prefixGuard)))
		emitLoop(indent + "\t")
		crdBuf.Write([]byte(fmt.Sprintf("%s}\n", indent)))
	} else {
		emitLoop(indent)
	}
}

func genCRDBlobFields(crdName string, crdMsg *protogen.Message, crdBuf *bytes.Buffer, extTypes *protoregistry.Types) {
	var blobFields []string
	processedMessageTypes := make(map[protogen.GoIdent]bool)
	findBlobFields(crdMsg, "", &blobFields, &processedMessageTypes, extTypes)

	hasBlobFields := len(blobFields) > 0
	typeInfo := struct {
		Name          string
		HasBlobFields bool
	}{crdName, hasBlobFields}
	templates.CRDHasBlobFields.Execute(crdBuf, typeInfo)
	crdBuf.Write([]byte("\n"))
	genClearCrdFields(crdName, blobFields, crdBuf)
	templates.CRDFillBlobFields.Execute(crdBuf, typeInfo)
	crdBuf.Write([]byte("\n"))
}

func genClearCrdFields(crdName string, blobFields []string, crdBuf *bytes.Buffer) {
	typeInfo := struct {
		Name string
	}{crdName}
	templates.CRDClearBlobFieldsHeader.Execute(crdBuf, typeInfo)

	for _, blobFieldName := range blobFields {
		if hasRepeatedSegment(blobFieldName) {
			// Path passes through one or more repeated fields — emit range loops.
			genClearRepeatedBlobField(parsePathSegments(blobFieldName), "m", "\t", 0, crdBuf)
		} else {
			crdBuf.Write([]byte(fmt.Sprintf("\tif %s {\n", getIfNestedFieldsNotNil(blobFieldName))))
			statement := fmt.Sprintf("\t\tm.%s = nil\n", blobFieldName)
			crdBuf.Write([]byte(statement))
			crdBuf.Write([]byte("\t}\n"))
		}
	}

	crdBuf.Write([]byte("}\n\n"))
}

func getIfNestedFieldsNotNil(blobFieldName string) string {
	paths := strings.Split(blobFieldName, ".")
	statement := ""
	cur := fmt.Sprintf("m.%s", paths[0])
	for i := 1; i < len(paths); i++ {
		cur += fmt.Sprintf(".%s", paths[i])
		// `Spec` and `Status` are not pointer type so we should skip generating `!= nil` statement
		if paths[i] == "Spec" || paths[i] == "Status" {
			continue
		}
		if len(statement) > 0 {
			statement += " && "
		}
		statement += fmt.Sprintf("%s != nil", cur)
	}
	return statement
}

func protoFileName(goFileName string) string {
	return string(_reProtoFileName.ReplaceAll([]byte(goFileName), []byte(".proto")))
}

func generate(reqData []byte) *plugingo.CodeGeneratorResponse {
	// generate go code with gogoproto
	resp := gogoproto(reqData)

	gofiles := make(map[string]*plugingo.CodeGeneratorResponse_File)
	for _, f := range resp.File {
		gofiles[protoFileName(f.GetName())] = f
	}

	// parse protoc request with golang proto
	// golang proto api is easier to use than gogo proto api
	var req pluginpb.CodeGeneratorRequest
	golangproto.Unmarshal(reqData, &req)
	util.ReplaceImportPath(&req)

	// initialize golang proto generator
	gen, err := protogen.Options{}.New(&req)
	if err != nil {
		logger.Panicf("failed to initialize golang proto generator %v", err)
	}

	// Load protobuf extensions from all the imported protobuf files
	extTypes := pboptions.LoadPBExtensions(gen.Files)

	// Load group info
	gInfoMap := groupinfo.Load(gen, extTypes)

	processedGoPackageList := make(map[string]bool)
	for _, f := range gen.Files {
		// skip the proto file that don't need to generate go code,
		// such as imported proto files
		if !f.Generate {
			continue
		}
		gInfo, ok := gInfoMap[string(f.Desc.Package())]
		if !ok {
			logger.Panicln(fmt.Sprintf("Failed to derive API group version info for protobuf package: %v. "+
				"Make sure to define groupversion_info.proto for the API group.", f.Desc.Package()))
		}
		updateGoFile(gofiles[f.Proto.GetName()], f, extTypes, gInfo, &processedGoPackageList)
	}
	return resp
}

// findEnumTypeFields recursively finds all enum fields within a given protobuf message and its nested messages.
//
// This function:
// - Traverses through all fields of the given message (`curMsg`).
// - Identifies enum fields and adds them to `enumTypeFields`, ensuring they are not duplicated.
// - Recursively processes nested messages to extract enum fields from them as well.
// - Maintains a record of processed message types and Go package imports to avoid redundant processing.
//
// Args:
//
//	curMsg                 *protogen.Message     - The current protobuf message being analyzed.
//	pathPrefix             string               - The prefix representing the field hierarchy (used for recursion).
//	enumTypeFields             *[]string            - A list to store discovered enum type names.
//	processedMessageTypes  *map[protogen.GoIdent]bool - Tracks already processed message types to prevent cycles.
//	extTypes               *protoregistry.Types - A registry of external types (not used in this function but may be useful for extensions).
//	processedGoPackageList *map[string]bool     - A map to track processed Go packages and prevent duplicate enum field entries.
func findEnumTypeFields(curMsg *protogen.Message, pathPrefix string, enumTypeFields *[]string,
	processedMessageTypes *map[protogen.GoIdent]bool, extTypes *protoregistry.Types, processedGoPackageList *map[string]bool) {
	for _, field := range curMsg.Fields {
		// Check if this field is an enum
		if field.Enum != nil {
			// Only include enums that are declared in the same package (e.g. michelangelo.test.kubeproto)
			// to avoid generating UnmarshalJSON methods for external package enums that are not imported
			if field.Enum.GoIdent.GoImportPath == curMsg.GoIdent.GoImportPath {
				key := fmt.Sprintf("%s.%s", field.Enum.GoIdent.GoImportPath, field.Enum.GoIdent.GoName)
				if _, found := (*processedGoPackageList)[key]; !found {
					(*processedGoPackageList)[key] = true
					newPrefix := strings.Trim(field.Enum.GoIdent.GoName, ".")
					*enumTypeFields = append(*enumTypeFields, newPrefix)
				}
			}
		}
		// Continue processing nested messages
		if field.Message != nil && field.Message.GoIdent.GoImportPath == curMsg.GoIdent.GoImportPath {
			if _, found := (*processedMessageTypes)[field.Message.GoIdent]; !found {
				(*processedMessageTypes)[field.Message.GoIdent] = true
				findEnumTypeFields(field.Message, pathPrefix+"."+field.GoName, enumTypeFields, processedMessageTypes,
					extTypes, processedGoPackageList)
				delete(*processedMessageTypes, field.Message.GoIdent)
			}
		}
	}
}

// genCRDEnumTypeFields generates UnmarshalJSON methods for all enum fields in a given CRD (Custom Resource Definition) message.
//
// This function:
// - Identifies all enum fields within the provided CRD protobuf message (`crdMsg`).
// - Uses `findEnumTypeFields` to traverse nested messages and collect enum field names.
// - Iterates through the discovered enum fields and generates UnmarshalJSON methods using a template.
// - Writes the generated code to the provided buffer (`crdBuf`).
//
// Args:
//
//	crdName                string                    - The name of the CRD being processed.
//	crdMsg                 *protogen.Message         - The protobuf message representing the CRD.
//	crdBuf                 *bytes.Buffer            - The buffer to write the generated code to.
//	extTypes               *protoregistry.Types     - A registry of external types (may be useful for future extensions).
//	processedGoPackageList *map[string]bool         - A map to track processed Go packages to avoid redundant processing.
func genCRDEnumTypeFields(crdName string, crdMsg *protogen.Message, crdBuf *bytes.Buffer, extTypes *protoregistry.Types,
	processedGoPackageList *map[string]bool,
) {
	processedMessageTypes := make(map[protogen.GoIdent]bool)
	enumTypeFields := make([]string, 0)
	// Find all enum fields in the CRD message
	findEnumTypeFields(crdMsg, "", &enumTypeFields, &processedMessageTypes, extTypes, processedGoPackageList)

	// Generate UnmarshalJSON methods for each enum field
	for _, enumTypeName := range enumTypeFields {
		typeInfo := struct {
			EnumTypeName string
		}{
			EnumTypeName: enumTypeName,
		}
		templates.CRDUnmarshalEnum.Execute(crdBuf, typeInfo)
		crdBuf.Write([]byte("\n"))
	}
}

