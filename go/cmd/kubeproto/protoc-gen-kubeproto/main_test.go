func TestBlobFields(t *testing.T) {
	testMsg := &testpb.TestMsg{
		Name: "testAnyMessage",
		Num:  5,
	}
	any, _ := types.MarshalAny(testMsg)
	testMsg3 := &testpb.TestMsg3{
		ObjectMeta: v1.ObjectMeta{
			Namespace:       "TestProject",
			Name:            "TestObj",
			ResourceVersion: "abcde",
		},
		Spec: testpb.TestMsg3Spec{
			Any: any,
		},
		Status: testpb.TestMsg3Status{
			Any: any,
		},
	}
	blobObj := &testpb.TestObject{
		ObjectMeta: v1.ObjectMeta{
			Namespace:       "TestProject",
			Name:            "TestObj",
			ResourceVersion: "abcde",
		},
		Spec: testpb.TestObjectSpec{
			NestedEnum: testpb.CLUSTER_TYPE_PELOTON,
			Any:        any,
			Msg_3:      testMsg3,
		},
	}
	assert.True(t, blobObj.HasBlobFields())
	expectedObj1, expectedObj2 := blobObj.DeepCopy(), blobObj.DeepCopy()
	expectedObj1.Spec.Any = nil
	expectedObj1.Spec.Msg_3.Spec.Any = nil
	expectedObj1.Spec.Msg_3.Status.Any = nil
	blobObj.ClearBlobFields()
	assert.Equal(t, expectedObj1, blobObj)

	blobObj.ObjectMeta.ResourceVersion = "zzzzz"
	blobObj.FillBlobFields(expectedObj2)
	expectedObj2.ObjectMeta.ResourceVersion = "zzzzz"
	assert.Equal(t, expectedObj2, blobObj)
}

