type goFileInfo struct {
	imports []string
	structs map[string]*dst.StructType
}

func getImports(file *dst.File) []string {
	var imports []string
	for _, i := range file.Imports {
		name := i.Name
		if name != nil {
			imports = append(imports, name.Name+" "+i.Path.Value)
		} else {
			imports = append(imports, i.Path.Value)
		}
	}
	return imports
}

func getStructs(file *dst.File) map[string]*dst.StructType {
	structs := map[string]*dst.StructType{}
	for _, decl := range file.Decls {
		genDecl, isGenDecl := decl.(*dst.GenDecl)
		if isGenDecl {
			if genDecl.Tok == token.TYPE {
				typeSpec := genDecl.Specs[0].(*dst.TypeSpec)
				structType, isStruct := typeSpec.Type.(*dst.StructType)
				if isStruct {
					structs[typeSpec.Name.Name] = structType
				}
			}
		}
	}
	return structs
}

func parse(gofile string) goFileInfo {
	dst, _ := decorator.Parse(gofile)
	file := goFileInfo{}
	file.imports = getImports(dst)
	file.structs = getStructs(dst)
	return file
}

func TestGen(t *testing.T) {
	data := testpb.GetProtocReqData()
	resp := generate(data)

	var projectFile *plugin_go.CodeGeneratorResponse_File
	var groupInfoFile *plugin_go.CodeGeneratorResponse_File
	for _, f := range resp.GetFile() {
		if strings.HasSuffix(*f.Name, "project_ut.pb.go") {
			projectFile = f
		}
		if strings.HasSuffix(*f.Name, "groupversion_info_ut.pb.go") {
			groupInfoFile = f
		}
	}

	assert.True(t, projectFile != nil)
	c := projectFile.GetContent()
	p := parse(c)

	assert.Contains(t, p.imports, `metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"`)
	assert.Contains(t, p.imports, `k8sruntime "k8s.io/apimachinery/pkg/runtime"`)
	assert.Equal(t, 5, len(p.structs))

	assert.Contains(t, c, `SchemeBuilder.Register(&Project{})`)
	assert.Contains(t, c, `SchemeBuilder.Register(&ProjectList{})`)
	assert.Contains(t, c, `func (in *Project) DeepCopy() *Project`)
	assert.Contains(t, c, `func (in *ProjectList) DeepCopy() *ProjectList`)
	assert.Contains(t, c, `YamlSchemas["Project"] =`)
	assert.Contains(t, c, `name: projects-plural.michelangelo.api`)
	//TODO(https://github.com/uber/michelangelo/issues/65): assert.Contains(t, c, expectedCRDDescription)

	assert.True(t, groupInfoFile != nil)
	c = groupInfoFile.GetContent()
	g := parse(c)
	assert.Contains(t, g.imports, `"k8s.io/apimachinery/pkg/runtime/schema"`)
	assert.Contains(t, g.imports, `"sigs.k8s.io/controller-runtime/pkg/scheme"`)
	assert.Equal(t, 0, len(g.structs))
	assert.Contains(t, c, `GroupVersion = schema.GroupVersion{Group: "michelangelo.api", Version: "v2"}`)
	assert.Contains(t, c, `SchemeBuilder = &scheme.Builder{GroupVersion: GroupVersion}`)
	assert.Contains(t, c, `AddToScheme = SchemeBuilder.AddToScheme`)
}

