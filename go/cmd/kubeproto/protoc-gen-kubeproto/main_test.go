const ObjectJSON = `{"kind":"Object","metadata":{"name":"object01","namespace":"default","creationTimestamp":null},"spec":{"description":"test"},"status":{}}`
const EnumJSON = `{"inputSchema":{"schemaItems":[{"boolValue":true,"dataType":"DATA_TYPE_BOOLEAN"},{"int32Value":1234,"dataType":"DATA_TYPE_NUMERIC"}]},"nestedEnum":"CLUSTER_TYPE_PELOTON"}`
const AnyJSON = `{"any":{"@type":"type.googleapis.com/michelangelo.test.kubeproto.TestMsg","value":"CAUSDnRlc3RBbnlNZXNzYWdl"}}`
const TimeJSON = `{"time":"2021-06-07T09:01:02.000000003Z"}`
const StructJSON = `{"struct":{"boolValue":true,"listValue":[true,"ma20api",64],"nullValue":null,"numberValue":64,"stringValue":"ma20api","structValue":{"nestedNumberValue":64}}}`
const DurationJSON = `{"seconds":1000}`
const DurationJSON2 = `{"nanos":1000}`

func TestJSON(t *testing.T) {
	// Test inline field
	testObject := &testpb.TestObject{}
	testObject.Namespace = "default"
	testObject.Name = "object01"
	testObject.Kind = "Object"
	testObject.Spec.Description = "test"

	testObjectJSON, err := json.Marshal(testObject)
	assert.True(t, err == nil)
	assert.Equal(t, ObjectJSON, string(testObjectJSON))

	// Test enum type
	testEnum := &testpb.TestObjectSpec{
		NestedEnum: testpb.CLUSTER_TYPE_PELOTON,
		InputSchema: &testpb.DataSchema{
			SchemaItems: []*testpb.DataSchemaItem{
				{
					DataType:  testpb.DATA_TYPE_BOOLEAN,
					BoolValue: true,
				},
				{
					DataType:   testpb.DATA_TYPE_NUMERIC,
					Int32Value: 1234,
				},
			},
		},
	}

	testEnumJSON, err := json.Marshal(testEnum)
	assert.True(t, err == nil)
	assert.Equal(t, EnumJSON, string(testEnumJSON))

	// Test Any type
	testMsg := &testpb.TestMsg{
		Name: "testAnyMessage",
		Num:  5,
	}

	any, err := types.MarshalAny(testMsg)
	assert.True(t, err == nil)
	testAny := &testpb.TestObjectSpec{
		Any: any,
	}
	testAnyJSON, err := json.Marshal(testAny)
	assert.True(t, err == nil)
	assert.Equal(t, AnyJSON, string(testAnyJSON))

	// Test Timestamp type
	timestamp, err := types.TimestampProto(
		time.Date(2021, time.June, 7, 9, 1, 2, 3, time.UTC))
	assert.True(t, err == nil)

	testTimestamp := &testpb.TestObjectSpec{
		Time: timestamp,
	}
	testTimeJSON, err := json.Marshal(testTimestamp)
	assert.True(t, err == nil)
	assert.Equal(t, TimeJSON, string(testTimeJSON))

	// Test duration type
	testDuration := types.DurationProto(1000 * time.Second)
	testDurationJSON, err := json.Marshal(testDuration)
	assert.True(t, err == nil)
	assert.Equal(t, DurationJSON, string(testDurationJSON))

	// Test duration type
	testDuration2 := types.DurationProto(1000 * time.Nanosecond)
	testDurationJSON2, err := json.Marshal(testDuration2)
	assert.True(t, err == nil)
	assert.Equal(t, DurationJSON2, string(testDurationJSON2))

	tmp := &testpb.TestObjectSpec{}
	err = json.Unmarshal(testTimeJSON, tmp)
	assert.True(t, err == nil)
	assert.Equal(t, timestamp, tmp.Time)

	// Test Strut type
	testStructNum := 64.0
	testStructBool := true
	testStructString := "ma20api"

	testStructNestedStruct := &types.Struct{
		Fields: map[string]*types.Value{
			"nestedNumberValue": {Kind: &types.Value_NumberValue{NumberValue: testStructNum}},
		},
	}

	testStructList := &types.ListValue{
		Values: []*types.Value{
			{Kind: &types.Value_BoolValue{BoolValue: testStructBool}},
			{Kind: &types.Value_StringValue{StringValue: testStructString}},
			{Kind: &types.Value_NumberValue{NumberValue: testStructNum}},
		},
	}

	testStruct := &types.Struct{
		Fields: map[string]*types.Value{
			"nullValue":   {Kind: &types.Value_NullValue{}},
			"numberValue": {Kind: &types.Value_NumberValue{NumberValue: testStructNum}},
			"stringValue": {Kind: &types.Value_StringValue{StringValue: testStructString}},
			"boolValue":   {Kind: &types.Value_BoolValue{BoolValue: testStructBool}},
			"structValue": {Kind: &types.Value_StructValue{StructValue: testStructNestedStruct}},
			"listValue":   {Kind: &types.Value_ListValue{ListValue: testStructList}},
		},
	}

	testPbWithStruct := &testpb.TestObjectSpec{
		Struct: testStruct,
	}
	testStructJSON, err := json.Marshal(testPbWithStruct)
	assert.Nil(t, err)
	assert.Equal(t, StructJSON, string(testStructJSON))

	testPbWithStructDecode := &testpb.TestObjectSpec{}
	err = json.Unmarshal(testStructJSON, testPbWithStructDecode)
	assert.Nil(t, err)
	assert.Equal(t, testStructNum, testPbWithStructDecode.Struct.Fields["numberValue"].GetNumberValue())
	assert.Equal(t, types.NullValue_NULL_VALUE, testPbWithStructDecode.Struct.Fields["nullValue"].GetNullValue())
	assert.Equal(t, testStructString, testPbWithStructDecode.Struct.Fields["stringValue"].GetStringValue())
	assert.Equal(t, testStructBool, testPbWithStructDecode.Struct.Fields["boolValue"].GetBoolValue())
	assert.True(t, testStructNestedStruct.Equal(testPbWithStructDecode.Struct.Fields["structValue"].GetStructValue()))
	assert.True(t, testStructList.Equal(testPbWithStructDecode.Struct.Fields["listValue"].GetListValue()))
}

